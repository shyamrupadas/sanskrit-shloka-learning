import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const packageRoot = dirname(dirname(fileURLToPath(import.meta.url)));
const openApiPath = join(packageRoot, "generated", "openapi", "openapi.json");
const generatedRoot = join(packageRoot, "generated");

const openApi = JSON.parse(await readFile(openApiPath, "utf8"));

const header = `/* eslint-disable */\n// This file is generated by packages/api-contract/scripts/generate-contract.mjs.\n// Do not edit this file manually.\n\n`;

const schemas = openApi.components?.schemas ?? {};
const schemaNames = new Map(
  Object.keys(schemas).map((schemaName) => [schemaName, toTypeName(schemaName)]),
);
const operations = collectOperations(openApi.paths ?? {});

for (const operation of operations) {
  operation.handlerRequestType = `${capitalize(operation.methodName)}Request`;
  operation.handlerResponseType = `${capitalize(operation.methodName)}Response`;
}

await writeGenerated("frontend/types.ts", renderTypes());
await writeGenerated("frontend/client.ts", renderFrontendClient());
await writeGenerated("backend/handlers.ts", renderBackendHandlers());

async function writeGenerated(relativePath, content) {
  const outputPath = join(generatedRoot, relativePath);
  await mkdir(dirname(outputPath), { recursive: true });
  await writeFile(outputPath, `${header}${content}`, "utf8");
}

function collectOperations(paths) {
  const httpMethods = new Set(["get", "post", "put", "patch", "delete"]);
  const collected = [];

  for (const [path, pathItem] of Object.entries(paths)) {
    for (const [method, operation] of Object.entries(pathItem)) {
      if (!httpMethods.has(method)) {
        continue;
      }

      const operationId = operation.operationId ?? toOperationId(method, path);
      const requestBodySchema =
        operation.requestBody?.content?.["application/json"]?.schema;
      const responses = Object.entries(operation.responses ?? {}).map(
        ([status, response]) => ({
          status,
          bodySchema: response.content?.["application/json"]?.schema,
        }),
      );
      const parameters = operation.parameters ?? [];
      const pathParameters = parameters
        .filter((parameter) => parameter.in === "path")
        .map((parameter) => ({
          name: parameter.name,
          type: tsTypeFromSchema(parameter.schema ?? { type: "string" }),
        }));

      collected.push({
        method: method.toUpperCase(),
        methodName: methodNameFromRoute(method, path),
        operationId,
        path,
        pathParameters,
        requestBodyType: requestBodySchema
          ? tsTypeFromSchema(requestBodySchema)
          : undefined,
        responses,
        successType: successTypeFor(responses),
        responseType: responseUnionTypeFor(responses),
        requiresAuthorization: parameters.some(
          (parameter) =>
            parameter.in === "header" &&
            parameter.name.toLowerCase() === "authorization",
        ),
      });
    }
  }

  return collected.sort((a, b) => a.operationId.localeCompare(b.operationId));
}

function renderTypes() {
  const renderedSchemas = Object.entries(schemas)
    .map(([schemaName, schema]) => renderSchema(schemaName, schema))
    .join("\n\n");

  return `${renderedSchemas}\n`;
}

function renderSchema(schemaName, schema) {
  const typeName = schemaNames.get(schemaName) ?? toTypeName(schemaName);

  if (schema.enum) {
    const values = schema.enum.map((value) => JSON.stringify(value)).join(" | ");
    return `export type ${typeName} = ${values};`;
  }

  if (schema.const !== undefined) {
    return `export type ${typeName} = ${JSON.stringify(schema.const)};`;
  }

  if (schema.type === "object" || schema.properties) {
    const required = new Set(schema.required ?? []);
    const properties = Object.entries(schema.properties ?? {})
      .map(([propertyName, propertySchema]) => {
        const optional = required.has(propertyName) ? "" : "?";
        return `  ${quoteProperty(propertyName)}${optional}: ${tsTypeFromSchema(propertySchema)};`;
      })
      .join("\n");
    return `export interface ${typeName} {\n${properties}\n}`;
  }

  return `export type ${typeName} = ${tsTypeFromSchema(schema)};`;
}

function renderFrontendClient() {
  const methods = operations.map(renderClientMethod).join("\n\n");
  const apiErrorType = schemaNames.get("SanskritShlokaLearning.ApiError") ?? "ApiError";

  return `import type * as Types from "./types.js";

export interface ApiClientOptions {
  baseUrl?: string;
  accessToken?: string | (() => string | undefined);
  fetch?: typeof fetch;
}

export class ApiClient {
  readonly #baseUrl: string;
  readonly #accessToken: string | (() => string | undefined) | undefined;
  readonly #fetch: typeof fetch;

  constructor(options: ApiClientOptions = {}) {
    this.#baseUrl = options.baseUrl ?? "";
    this.#accessToken = options.accessToken;
    this.#fetch = options.fetch ?? fetch;
  }

${indent(methods, 2)}

  async #request<TResponse>(
    path: string,
    init: RequestInit & { body?: string } = {},
  ): Promise<TResponse> {
    const headers = new Headers(init.headers);
    const token = typeof this.#accessToken === "function" ? this.#accessToken() : this.#accessToken;

    if (token) {
      headers.set("Authorization", \`Bearer \${token}\`);
    }

    if (init.body && !headers.has("Content-Type")) {
      headers.set("Content-Type", "application/json");
    }

    const response = await this.#fetch(\`\${this.#baseUrl}\${path}\`, {
      ...init,
      headers,
    });

    if (response.status === 204) {
      return undefined as TResponse;
    }

    const body = await response.json();

    if (!response.ok) {
      throw new ApiError(response.status, body as Types.${apiErrorType});
    }

    return body as TResponse;
  }
}

export class ApiError extends Error {
  constructor(
    readonly status: number,
    readonly body: Types.${apiErrorType},
  ) {
    super(body.message);
  }
}
`;
}

function renderClientMethod(operation) {
  const parameters = [
    ...operation.pathParameters.map((parameter) => `${parameter.name}: ${parameter.type}`),
    ...(operation.requestBodyType ? [`request: Types.${operation.requestBodyType}`] : []),
  ].join(", ");
  const bodyInit = operation.requestBodyType
    ? ",\n    body: JSON.stringify(request)"
    : "";

  return `async ${operation.methodName}(${parameters}): Promise<${operation.successType}> {
  return this.#request<${operation.successType}>(${renderPathExpression(operation)}, {
    method: ${JSON.stringify(operation.method)}${bodyInit}
  });
}`;
}

function renderBackendHandlers() {
  const requestTypes = operations
    .map((operation) => {
      const body = operation.requestBodyType
        ? `\n  body: Types.${operation.requestBodyType};`
        : "";
      const path = operation.pathParameters
        .map((parameter) => `\n  ${parameter.name}: ${parameter.type};`)
        .join("");
      const authorization = operation.requiresAuthorization
        ? "\n  authorization?: string;"
        : "";
      return `export interface ${operation.handlerRequestType} {${body}${path}${authorization}\n}`;
    })
    .join("\n\n");

  const responseTypes = operations
    .map(
      (operation) =>
        `export type ${operation.handlerResponseType} = ${operation.responseType};`,
    )
    .join("\n");

  const handlerProperties = operations
    .map(
      (operation) =>
        `  ${operation.methodName}(request: ${operation.handlerRequestType}): Promise<${operation.handlerResponseType}> | ${operation.handlerResponseType};`,
    )
    .join("\n");

  return `import type * as Types from "../frontend/types.js";

export type ApiHandlerResponse<TStatus extends number, TBody = undefined> = TBody extends undefined
  ? { status: TStatus }
  : { status: TStatus; body: TBody };

${requestTypes}

${responseTypes}

export interface ApiHandlers {
${handlerProperties}
}
`;
}

function successTypeFor(responses) {
  const successResponse = responses.find(({ status }) => status.startsWith("2"));
  if (!successResponse?.bodySchema) {
    return "void";
  }
  return `Types.${tsTypeFromSchema(successResponse.bodySchema)}`;
}

function responseUnionTypeFor(responses) {
  return responses
    .map(({ status, bodySchema }) => {
      const numericStatus = Number(status);
      if (!bodySchema) {
        return `ApiHandlerResponse<${numericStatus}>`;
      }
      return `ApiHandlerResponse<${numericStatus}, Types.${tsTypeFromSchema(bodySchema)}>`;
    })
    .join(" | ");
}

function tsTypeFromSchema(schema) {
  if (schema.$ref) {
    const schemaName = schema.$ref.split("/").at(-1);
    return schemaNames.get(schemaName) ?? toTypeName(schemaName);
  }

  if (schema.const !== undefined) {
    return JSON.stringify(schema.const);
  }

  if (schema.enum) {
    return schema.enum.map((value) => JSON.stringify(value)).join(" | ");
  }

  if (schema.type === "array") {
    return `${tsTypeFromSchema(schema.items)}[]`;
  }

  if (schema.type === "object" || schema.properties) {
    const required = new Set(schema.required ?? []);
    const properties = Object.entries(schema.properties ?? {})
      .map(([propertyName, propertySchema]) => {
        const optional = required.has(propertyName) ? "" : "?";
        return `${quoteProperty(propertyName)}${optional}: ${tsTypeFromSchema(propertySchema)}`;
      })
      .join("; ");
    return `{ ${properties} }`;
  }

  if (schema.type === "integer" || schema.type === "number") {
    return "number";
  }

  if (schema.type === "boolean") {
    return "boolean";
  }

  return "string";
}

function toOperationId(method, path) {
  return `${method}_${path.replaceAll("/", "_").replaceAll("{", "").replaceAll("}", "")}`;
}

function methodNameFromRoute(method, path) {
  const parts = path.split("/").filter(Boolean);
  const contractParts = parts[0] === "api" ? parts.slice(1) : parts;
  const lastPart = contractParts.at(-1) ?? method;
  const isItemRoute = lastPart.startsWith("{") && lastPart.endsWith("}");
  const resourcePart = isItemRoute ? singularize(contractParts.at(-2) ?? lastPart) : lastPart;

  if (method === "get") {
    return `get${capitalize(toCamelCase(resourcePart))}`;
  }

  if (isItemRoute && method === "patch") {
    return `update${capitalize(toCamelCase(resourcePart))}`;
  }

  return toCamelCase(lastPart);
}

function renderPathExpression(operation) {
  let pathExpression = operation.path;

  for (const parameter of operation.pathParameters) {
    pathExpression = pathExpression.replaceAll(
      `{${parameter.name}}`,
      `\${encodeURIComponent(${parameter.name})}`,
    );
  }

  return pathExpression.includes("${")
    ? `\`${pathExpression}\``
    : JSON.stringify(pathExpression);
}

function singularize(value) {
  return value.endsWith("s") ? value.slice(0, -1) : value;
}

function toTypeName(schemaName) {
  const lastSegment = schemaName.split(".").filter(Boolean).at(-1) ?? schemaName;
  return capitalize(lastSegment);
}

function toCamelCase(value) {
  return value
    .split(/[^A-Za-z0-9]+/)
    .filter(Boolean)
    .map((part, index) =>
      index === 0
        ? `${part.charAt(0).toLowerCase()}${part.slice(1)}`
        : capitalize(part),
    )
    .join("");
}

function quoteProperty(name) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name) ? name : JSON.stringify(name);
}

function indent(text, spaces) {
  const prefix = " ".repeat(spaces);
  return text
    .split("\n")
    .map((line) => (line.length === 0 ? line : `${prefix}${line}`))
    .join("\n");
}

function capitalize(value) {
  return `${value.charAt(0).toUpperCase()}${value.slice(1)}`;
}
